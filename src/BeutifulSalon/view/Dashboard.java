    private void createChartPanel() { // this method will create the chart panel containin the graph 
    
        String chartTitle = "Objects Movement Chart";
    String xAxisLabel = "X";
    String yAxisLabel = "Y";
    
    XYDataset dataset = createDataset();
    
    JFreeChart chart =  ChartFactory.createXYLineChart(chartTitle, 
            xAxisLabel, yAxisLabel, dataset);

    
    // saves the chart as an image files

    jPanelGrafico.add(new ChartPanel(chart));
}

    private XYDataset createDataset() {    // this method creates the data as time seris 
    XYSeriesCollection dataset = new XYSeriesCollection();
    XYSeries series1 = new XYSeries("Object 1");
    XYSeries series2 = new XYSeries("Object 2");
    
    series1.add(1.0, 2.0);
    series1.add(2.0, 3.0);
    series1.add(3.0, 2.5);
    series1.add(3.5, 2.8);
    series1.add(4.2, 6.0);
    
    series2.add(2.0, 1.0);
    series2.add(2.5, 2.4);
    series2.add(3.2, 1.2);
    series2.add(3.9, 2.8);
    series2.add(4.6, 3.0);

    
    dataset.addSeries(series1);
    dataset.addSeries(series2);
    
    return dataset;
}
    

    
